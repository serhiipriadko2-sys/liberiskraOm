@dataclass
class SystemLoad:
    """–ù–∞–≥—Ä—É–∑–∫–∞ —Å–∏—Å—Ç–µ–º—ã"""
    timestamp: str
    cpu_percent: float
    memory_percent: float
    active_connections: int
    response_time_ms: float
    throughput: float  # requests per second

@dataclass
class StressTestResult:
    """–†–µ–∑—É–ª—å—Ç–∞—Ç —Å—Ç—Ä–µ—Å—Å-—Ç–µ—Å—Ç–∞"""
    duration_seconds: int
    concurrent_users: int
    peak_cpu: float
    peak_memory: float
    avg_response_time: float
    p95_response_time: float
    success_rate: float
    errors: int
    total_requests: int

def simulate_system_load(duration: int = 60, users: int = 100):
    """–°–∏–º—É–ª—è—Ü–∏—è –Ω–∞–≥—Ä—É–∑–∫–∏ —Å–∏—Å—Ç–µ–º—ã"""
    
    print(f"üîÑ –°–∏–º—É–ª—è—Ü–∏—è –Ω–∞–≥—Ä—É–∑–∫–∏: {users} –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–π –Ω–∞ {duration} —Å–µ–∫—É–Ω–¥...")
    
    load_data = []
    response_times = []
    requests_count = 0
    errors_count = 0
    
    start_time = time.time()
    end_time = start_time + duration
    
    # –°–∏–º—É–ª—è—Ü–∏—è —Ä–∞–±–æ—Ç—ã –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–π
    while time.time() < end_time:
        current_time = time.time()
        elapsed = current_time - start_time
        
        # –°–∏–º—É–ª—è—Ü–∏—è —Å–∏—Å—Ç–µ–º–Ω–æ–π –Ω–∞–≥—Ä—É–∑–∫–∏
        cpu_load = random.uniform(20, 80)
        memory_load = random.uniform(40, 75)
        
        # –°–∏–º—É–ª—è—Ü–∏—è –æ–±—Ä–∞–±–æ—Ç–∫–∏ –∑–∞–ø—Ä–æ—Å–æ–≤
        for user in range(min(users, 10)):  # –û–≥—Ä–∞–Ω–∏—á–∏–≤–∞–µ–º –¥–ª—è –¥–µ–º–æ
            try:
                req_start = time.time()
                
                # –°–∏–º—É–ª—è—Ü–∏—è –æ–±—Ä–∞–±–æ—Ç–∫–∏ –∑–∞–ø—Ä–æ—Å–∞ (–±–∞–∑–∞ –¥–∞–Ω–Ω—ã—Ö + API + WebSocket)
                time.sleep(0.08 + random.uniform(-0.02, 0.02))  # ~80ms –æ–±—Ä–∞–±–æ—Ç–∫–∞
                
                req_time = (time.time() - req_start) * 1000
                response_times.append(req_time)
                requests_count += 1
                
            except Exception:
                errors_count += 1
        
        # –ó–∞–ø–∏—Å—å –º–µ—Ç—Ä–∏–∫ –∫–∞–∂–¥—É—é —Å–µ–∫—É–Ω–¥—É
        if elapsed >= len(load_data):
            load_data.append(SystemLoad(
                timestamp=datetime.fromtimestamp(current_time).isoformat(),
                cpu_percent=cpu_load,
                memory_percent=memory_load,
                active_connections=random.randint(users//2, users),
                response_time_ms=statistics.mean(response_times[-10:]) if response_times else 0,
                throughput=len(response_times) / elapsed if elapsed > 0 else 0
            ))
        
        time.sleep(0.1)  # –ò–Ω—Ç–µ—Ä–≤–∞–ª —Å–∏–º—É–ª—è—Ü–∏–∏
    
    # –ê–Ω–∞–ª–∏–∑ —Ä–µ–∑—É–ª—å—Ç–∞—Ç–æ–≤
    if response_times:
        avg_response = statistics.mean(response_times)
        p95_response = sorted(response_times)[int(len(response_times) * 0.95)]
        max_response = max(response_times)
    else:
        avg_response = p95_response = max_response = 0
    
    success_rate = ((requests_count - errors_count) / requests_count * 100) if requests_count > 0 else 0
    
    return StressTestResult(
        duration_seconds=duration,
        concurrent_users=users,
        peak_cpu=max(ld.cpu_percent for ld in load_data),
        peak_memory=max(ld.memory_percent for ld in load_data),
        avg_response_time=avg_response,
        p95_response_time=p95_response,
        success_rate=success_rate,
        errors=errors_count,
        total_requests=requests_count
    ), load_data

